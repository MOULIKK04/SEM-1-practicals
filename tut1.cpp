#include <iostream>
using namespace std;
int main() {

    int a,b;
    a=5;
    b=4;
    cout<<"the value of a+b is :"<<a+b<<endl;
    cout<<"the value of a-b is :"<<a-b<<endl;
    cout<<"the value of a/b is :"<<a/b<<endl;
    cout<<"the value of a*b is :"<<a*b<<endl;
    cout<<"the value of a++ is :"<<a++<<endl;
    cout<<"the value of a is :"<<a<<endl;
    cout<<"the value of ++a is :"<<++a<<endl;
    cout<<"the value of --a is :"<<--a<<endl;
    cout<<"the value of a-- is :"<<a--<<endl;




    return 0;
}